% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
R = str2num(get(handles.edit1,'String'))*1000;
C = str2num(get(handles.edit2,'String'))*(1/1000000);
L = str2num(get(handles.edit3,'String'))*(1/1000);
A = str2num(get(handles.edit4,'String'));
f = 50;
w = 2*pi*f;
t=0:0.001:0.06;
a = sin(w*t);
vI = A*a;
s=complex(0,w);
z = (w*L)/(1-w^2*L*C);
Z = complex(0,z);
vo = z/(R+Z)*vI;
vO = vo/a;
i = vo/(R+Z);
I = i/a;
I1 = 1/(1-w^2*L*C)*I;
I2 = I-I1;
set(handles.text20,'String',I1);
set(handles.text23,'String',I2);
set(handles.text22,'String',vO);
figure();
plot(t,vI,t,vo);
legend('vI(t)','vO(t)');
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


