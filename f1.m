% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
R = str2num(get(handles.edit1,'String'))*1000;
R1 = str2num(get(handles.edit2,'String'))*1000;
R2 = str2num(get(handles.edit3,'String'))*1000;
A = str2num(get(handles.edit4,'String'));
f = 50;
w = 2*pi*f;
t=0:0.001:0.06;
a = sin(w*t);
vI= A*a;
Rp=(R1*R2)/(R1+R2);
I = vI/(R+Rp);
i1 = (R2/(R1+R2))*I;
i2 = (R1/(R1+R2))*I;
vo = (Rp/(R+Rp))*vI;
vO = vo/a;
I1 = i1/a*1000;
I2 = i2/a*1000;
set(handles.text21,'String',I1);
set(handles.text22,'String',I2);
set(handles.text23,'String',vO);
figure();
plot(t,vI,t,vo);
legend('vI(t)','vO(t)');
