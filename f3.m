% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
R = str2num(get(handles.edit1,'String'))*1000;
R1 = str2num(get(handles.edit2,'String'))*1000;
L = str2num(get(handles.edit3,'String'))*(1/1000);
A = str2num(get(handles.edit4,'String'));
f = 50;
w = 2*pi*f;
t=0:0.001:0.06;
a = sin(w*t);
vI = A*a;
s=complex(0,w);
z1 = (R1*L^2*w^2)/(R1^2+w^2*L^2);
z2 = (R1^2*w*L)/(R1^2+w^2*L^2);
Z = complex(z1,z2);
vo = (Z/(R+Z))*vI;
vO = vo/a;
I = vO/(R+Z)*1000;
I1 = vO/R1*1000; 
I2 = I-I1;
set(handles.text19,'String',I1);
set(handles.text18,'String',I2);
set(handles.text17,'String',vO);
figure();
plot(t,vI,t,vo);
legend('vI(t)','vO(t)');
