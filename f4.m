% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
C = str2num(get(handles.edit2,'String'))*(1/1000000);
R1 = str2num(get(handles.edit3,'String'))*1000;
R2 = str2num(get(handles.edit4,'String'))*1000;
A = str2num(get(handles.edit5,'String'));
f = 50;
w = 2*pi*f;
t=0:0.001:0.06;
a = sin(w*t);
vI = A*a;
s=complex(0,w);
Z = (R1*R2)/(R1+R2);
z1 = (w^2*C^2*Z^2)/(1+w^2*Z^2*C^2);
z2 = (w*Z*C)/(1+w^2*Z^2*C^2);
Zech = complex(z1,z2);
vo = Zech*vI;
vO = vo/a;
I1 = (vO/R1)*1000;
I2 = (vO/R2)*1000;
set(handles.text17,'String',I1);
set(handles.text19,'String',I2);
set(handles.text18,'String',vO);
figure();
plot(t,vI,t,vo);
legend('vI(t)','vO(t)');
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


